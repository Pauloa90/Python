numero = int(input('Digite um valor: '))
dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)
print('O numero digitado foi: {}'.format(numero))
print('O dobro do numrero é escolhido: {} '.format(dobro))
print('O triplo do numero é: {} '.format(triplo))
print('A raiz quadrada é: {:.2f}'.format(raiz))